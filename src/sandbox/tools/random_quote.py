import random

TOOL_NAME = "random_quote"
TOOL_DESCRIPTION = "Повертає випадкову мотиваційну цитату для натхнення."
TOOL_PARAMETERS = {
    "type": "object",
    "properties": {},
    "required": []
}

def execute() -> str:
    quotes = [
        "Єдиний спосіб робити великі справи — любити те, що ви робите. — Стів Джобс",
        "Повір, що ти можеш, і ти вже на півшляху. — Теодор Рузвельт",
        "Успіх — це не фінал, невдача — не фатальна: важливою є мужність продовжувати. — Вінстон Черчилль",
        "Не дивись на годинник; роби те, що він робить. Продовжуй іти. — Сем Левенсон",
        "Дій так, ніби те, що ти робиш, має значення. Бо це так і є. — Вільям Джеймс",
        "Майбутнє належить тим, хто вірить у красу своїх мрій. — Елеонора Рузвельт",
        "Це завжди здається неможливим, поки не зроблено. — Нельсон Мандела",
        "Ваш час обмежений, тому не витрачайте його, живучи чужим життям. — Стів Джобс"
    ]
    return random.choice(quotes)

def test_random_quote():
    result = execute()
    assert isinstance(result, str)
    assert len(result) > 0
    print("Тест пройдено: Отримано цитату -", result)
